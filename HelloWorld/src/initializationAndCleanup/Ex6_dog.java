package initializationAndCleanup;

public class Ex6_dog {
	
	public void bark (byte b){
		System.out.println("byte - bark");
	}
	
	public void bark (char c){
		System.out.println("char - howl");
	}
	
	public void bark (short s){
		System.out.println("short - cry");
	}
	
	public void bark (int i){
		System.out.println("int - woof woof");
	}
	
	public void bark (long l){
		System.out.println("long - шезлонг");
	}
	
	public void bark (float f){
		System.out.println("float - piy piy");
	}
	
	public void bark (double d){
		System.out.println("double- шмабл");
	}
	
	public void bark (int i, long d){
		System.out.println("int + long большой шезлонг");
	}
	
	public void bark (long d, int i){
		System.out.println("long + int иди на фтинт");
	}
}
